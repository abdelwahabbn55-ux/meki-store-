async function fetchProducts() {
    const { data, error } = await supabase
        .from('products')
        .select('*')
        .order('created_at', { ascending: false });

    if (error) {
        console.error('Error fetching dynamic products:', error);
        return;
    }

    // Transform Supabase record to expected UI format
    products = data.map(p => ({
        id: p.id,
        name: { ar: p.name_ar, fr: p.name_fr, en: p.name_en },
        price: p.price,
        img: p.image_url,
        desc: { ar: p.desc_ar || '', fr: p.desc_fr || '', en: p.desc_en || '' }
    }));

    updateUI();
}

function updateUI() {
    document.documentElement.lang = currentLang;
    document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
    document.getElementById('lang-btn').textContent = currentLang.toUpperCase();

    // Update static translations
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            el.textContent = translations[currentLang][key];
        }
    });

    renderProducts();
}

function renderProducts() {
    const list = document.getElementById('product-list');
    list.innerHTML = '';

    products.forEach(p => {
        const card = document.createElement('div');
        card.className = 'product-card';
        card.setAttribute('data-reveal', '');
        card.innerHTML = `
            <img src="${p.img}" alt="${p.name[currentLang]}" class="product-img">
            <div class="product-info">
                <h3>${p.name[currentLang]}</h3>
                <p class="product-price">${p.price}</p>
                <button class="btn btn-primary add-to-cart" data-id="${p.id}">
                    ${translations[currentLang].add_to_cart}
                </button>
            </div>
        `;
        list.appendChild(card);
    });

    // Re-trigger scroll reveal since new elements were added
    initReveal();
}

function initReveal() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('revealed');
            }
        });
    }, { threshold: 0.1 });

    document.querySelectorAll('[data-reveal]').forEach(el => {
        observer.observe(el);
    });
}

